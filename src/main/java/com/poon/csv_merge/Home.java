    private void jFileChooser1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jFileChooser1ActionPerformed
        String command = evt.getActionCommand();
        if(command.contains("ApproveSelection")) {
            fileList = cm.addList(jFileChooser1.getSelectedFile().toString());
        } else {
            fileList = cm.removeList(jFileChooser1.getSelectedFile().toString());
        }
        
        Object[] test = fileList.toArray();
        DefaultListModel dlm = new DefaultListModel();
        for(Object test1 : test) {
            dlm.addElement(test1);
        }
        jList1.removeAll();
        jList1.setModel(dlm);
    }//GEN-LAST:event_jFileChooser1ActionPerformed

